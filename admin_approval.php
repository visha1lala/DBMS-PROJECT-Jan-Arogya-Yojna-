        <header>
            Jan Aarogya Yojna also referred to as (IIItdm Bharat Health Protection Scheme or IBHPS) is a national public
            health insurance fund of the IIITDM under Government of India that aims to provide free access to health
            insurance coverage for low income earners in the country. Roughly, the bottom 50% of the country qualifies
            for this scheme. People using the program access their own primary care services from a family doctor. When
            anyone needs additional care, then this scheme provides free secondary health care for those needing
            specialist treatment and tertiary health care for those requiring hospitalization.
            <br>
            <br>


            The programme is part of the IIITDM under Indian government's National Health Policy and is means-tested. It
            was launched in September 2022 by the Ministry of Health and Family Welfare. That ministry later established
            the National Health Authority as an organization to administer the program. It is a centrally sponsored
            scheme and is jointly funded by both the union government and the states. By offering services to 50 crore
            (500 million) people it is the world's largest government sponsored healthcare program. The program is a
            means-tested as its users are people with low income in India.
